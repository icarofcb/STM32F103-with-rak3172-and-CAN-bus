float onBoardTmp(void)
{//INSPIRATION AND TUTORIAL: https://www.digikey.com/en/maker/projects/getting-started-with-stm32-i2c-example/ba8c2bfef2024654b5dd10012425fa23

	static const uint8_t TMP102_ADDR = 0x48 << 1; // Use 8-bit address
	static const uint8_t REG_TEMP 	 = 0x00;

	HAL_StatusTypeDef ret;
	uint8_t buf[12];
	int16_t tempRaw;
	float tempCelsius;

	// Tell TMP102 that we want to read from the temperature register
	buf[0] = REG_TEMP;

	ret = HAL_I2C_Master_Transmit(&hi2c1, TMP102_ADDR, buf, 1, HAL_MAX_DELAY);

	if ( ret != HAL_OK )
	{

		//strcpy((char*)buf, "Error Tx\r\n");
		//I have yet to make something about erros

	}else
	{

		// Read 2 bytes from the temperature register
		ret = HAL_I2C_Master_Receive(&hi2c1, TMP102_ADDR, buf, 2, HAL_MAX_DELAY);

		if ( ret != HAL_OK ) {
			//strcpy((char*)buf, "Error Rx\r\n");
			//I have yet to make something about erros

	}else
		{
			//	Combine the bytes
			tempRaw = ((int16_t)buf[0] << 4) | (buf[1] >> 4);

			// Convert to 2's complement, since temperature can be negative
			if ( tempRaw > 0x7FF )
			{
				tempRaw |= 0xF000;
			}

			// Convert to float temperature value (Celsius)
			tempCelsius = tempRaw * 0.0625;

		}
	}

	//return the float value of temperature
	return tempCelsius;
}

